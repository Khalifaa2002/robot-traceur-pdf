"""
planning/trajectory_generator.py
================================
Converts raw pixel coordinates into world-space trajectories.

This module handles:
- Coordinate conversion (pixel to meter)
- Trajectory smoothing (interpolation)
- Orientation calculation (yaw angles)
- Tool offset compensation (geometric transform)

Status: Production (migrated from v3.1 core)
"""

import numpy as np
import matplotlib
from matplotlib import pyplot as plt
import os
from pathlib import Path

# Headless backend configuration for Raspberry Pi (no display)
if os.environ.get('DISPLAY') is None:
    matplotlib.use('Agg')

def pixel_to_world(points, pixel_to_meter=0.001, origin=(0, 0)):
    """
    Convert pixel coordinates to real-world meters.
    
    Default scaling assumes 1 pixel = 1 mm.
    """
    min_x = points[:, 0].min()
    min_y = points[:, 1].min()
    
    world_points = points.copy()
    world_points[:, 0] = (points[:, 0] - min_x) * pixel_to_meter + origin[0]
    world_points[:, 1] = (points[:, 1] - min_y) * pixel_to_meter + origin[1]
    
    return world_points

from planning.cubic_spline import CubicSpline2D, calc_spline_course

def smooth_trajectory(points, num_points=100, method='spline', ds=0.01):
    """
    Resample the trajectory with uniform spacing.
    Methods: 'linear' or 'spline' (default).
    """
    if method == 'linear':
        distances = np.sqrt(np.sum(np.diff(points, axis=0)**2, axis=1))
        cumulative_distance = np.concatenate(([0], np.cumsum(distances)))
        
        total_distance = cumulative_distance[-1]
        new_distances = np.linspace(0, total_distance, num_points)
        
        smooth_x = np.interp(new_distances, cumulative_distance, points[:, 0])
        smooth_y = np.interp(new_distances, cumulative_distance, points[:, 1])
        
        return np.column_stack((smooth_x, smooth_y))
    else:
        # Use Cubic Spline 2D for high-quality C2 smoothing
        # Note: CubicSpline2D needs unique points (roughly)
        x = points[:, 0]
        y = points[:, 1]
        
        # Remove consecutive duplicates which break spline
        dx = np.diff(x)
        dy = np.diff(y)
        mask = np.ones(len(x), dtype=bool)
        mask[1:] = (np.abs(dx) > 1e-6) | (np.abs(dy) > 1e-6)
        x, y = x[mask], y[mask]
        
        if len(x) < 2:
            return np.column_stack((x, y))
            
        # Calculate course
        rx, ry, ryaw, rk, s = calc_spline_course(x, y, ds=ds)
        
        # Resample to num_points if requested, or keep the ds-based result
        resampled_indices = np.linspace(0, len(rx)-1, num_points, dtype=int)
        return np.column_stack((np.array(rx)[resampled_indices], np.array(ry)[resampled_indices]))

def add_orientation(points, method='spline'):
    """
    Calculate the yaw angle (heading) for each point in a 2D path.
    If points were generated by a spline, we should ideally use spline yaw.
    """
    if method == 'spline':
        # Re-run spline to get yaw if not already provided
        x, y = points[:, 0], points[:, 1]
        sp = CubicSpline2D(x, y)
        
        points_with_angle = np.zeros((len(points), 3))
        points_with_angle[:, 0] = x
        points_with_angle[:, 1] = y
        
        for i in range(len(points)):
            # We need the arc length 's' for each point
            # For uniform points, we can approximate s
            # But it's safer to just calculate it
            if i == 0:
                s = 0.0
            else:
                s += np.sqrt((x[i]-x[i-1])**2 + (y[i]-y[i-1])**2)
            points_with_angle[i, 2] = sp.calc_yaw(s)
            
        return points_with_angle
    else:
        # Fallback to simple finite difference heading
        points_with_angle = np.zeros((len(points), 3))
        points_with_angle[:, :2] = points
        
        for i in range(len(points) - 1):
            dx = points[i+1, 0] - points[i, 0]
            dy = points[i+1, 1] - points[i, 1]
            points_with_angle[i, 2] = np.arctan2(dy, dx)
        
        if len(points) > 1:
            points_with_angle[-1, 2] = points_with_angle[-2, 2]
        
        return points_with_angle

def apply_tool_offset(points_with_angle, offset_x=0.0, offset_y=0.0):
    """
    Offset the robot's center path so that a tool at (offset_x, offset_y)
    relative to the robot center follows the target trajectory.
    """
    if offset_x == 0.0 and offset_y == 0.0:
        return points_with_angle
        
    offset_points = np.copy(points_with_angle)
    theta = points_with_angle[:, 2]
    
    dx_world = offset_x * np.cos(theta) - offset_y * np.sin(theta)
    dy_world = offset_x * np.sin(theta) + offset_y * np.cos(theta)
    
    offset_points[:, 0] -= dx_world
    offset_points[:, 1] -= dy_world
    
    return offset_points

def visualize_trajectory(original_points, smooth_points, world_points, points_with_angle):
    """Multi-stage visualization of the trajectory generation process."""
    fig, axes = plt.subplots(2, 2, figsize=(14, 12))
    
    # 1. Raw Pixels
    ax = axes[0, 0]
    ax.plot(original_points[:, 0], original_points[:, 1], 'ro-', linewidth=2, label='Extracted')
    ax.set_title("1. Raw PDF Pixels")
    ax.grid(True); ax.axis('equal')
    
    # 2. Smoothed Pixels
    ax = axes[0, 1]
    ax.plot(original_points[:, 0], original_points[:, 1], 'ro', alpha=0.3)
    ax.plot(smooth_points[:, 0], smooth_points[:, 1], 'b-', linewidth=2, label='Smooth')
    ax.set_title("2. Interpolated Smoothing")
    ax.grid(True); ax.axis('equal')
    
    # 3. Meters
    ax = axes[1, 0]
    ax.plot(world_points[:, 0], world_points[:, 1], 'g-', linewidth=2)
    ax.set_title("3. World Coordinates (meters)")
    ax.grid(True); ax.axis('equal')
    
    # 4. Heading
    ax = axes[1, 1]
    ax.plot(world_points[:, 0], world_points[:, 1], 'g-', alpha=0.3)
    step = max(1, len(points_with_angle) // 15)
    for i in range(0, len(points_with_angle), step):
        x, y, theta = points_with_angle[i]
        ax.arrow(x, y, 0.05*np.cos(theta), 0.05*np.sin(theta), head_width=0.01, fc='blue')
    ax.set_title("4. Calculated Headings")
    ax.grid(True); ax.axis('equal')
    
    plt.tight_layout()
    plt.show()

def save_trajectory(world_points, filename="data/trajectory.npy"):
    """Save trajectory to NPY and CSV formats."""
    os.makedirs(os.path.dirname(filename), exist_ok=True)
    np.save(filename, world_points)
    
    csv_filename = filename.replace('.npy', '.csv')
    np.savetxt(csv_filename, world_points, delimiter=',', header="x,y,theta", comments="")
    print(f"✅ Trajectory saved to {filename} and {csv_filename}")
